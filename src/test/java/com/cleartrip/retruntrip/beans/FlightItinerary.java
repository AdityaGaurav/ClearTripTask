package com.cleartrip.retruntrip.beans;

public class FlightItinerary {
    private String flightName;
    private String flightNumber;
    private String flightArrivalTime;
    private String flightDepartTime;
    private String flightDuration;
    private String flightDepartSource;
    private String flightArrivalDest;

    public String getFlightName() {
        return flightName;
    }

    public void setFlightName(String flightName) {
        this.flightName = flightName;
    }

    public String getFlightNumber() {
        return flightNumber;
    }

    public void setFlightNumber(String flightNumber) {
        this.flightNumber = flightNumber;
    }

    public String getFlightArrivalTime() {
        return flightArrivalTime;
    }

    public void setFlightArrivalTime(String flightArrivalTime) {
        this.flightArrivalTime = flightArrivalTime;
    }

    public String getFlightDepartTime() {
        return flightDepartTime;
    }

    public void setFlightDepartTime(String flightDepartTime) {
        this.flightDepartTime = flightDepartTime;
    }

    public String getFlightDuration() {
        return flightDuration;
    }

    public void setFlightDuration(String flightDuration) {
        this.flightDuration = flightDuration;
    }

    public String getFlightDepartSource() {
        return flightDepartSource;
    }

    public void setFlightDepartSource(String flightDepartSource) {
        this.flightDepartSource = flightDepartSource;
    }

    public String getFlightArrivalDest() {
        return flightArrivalDest;
    }

    public void setFlightArrivalDest(String flightArrivalDest) {
        this.flightArrivalDest = flightArrivalDest;
    }

}
